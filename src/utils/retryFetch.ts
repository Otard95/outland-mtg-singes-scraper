interface RetryFetchOptions {
  maxRetries?: number;
  retryDelay?: number;
}

const defaultOptions: Required<RetryFetchOptions> = {
  maxRetries: 3,
  retryDelay: 1000,
};

export function createFetch(
  options?: RetryFetchOptions
): (url: RequestInfo, init?: RequestInit) => Promise<Response> {
  const { maxRetries, retryDelay } = { ...defaultOptions, ...(options || {}) };
  const timeout = (retries: number) => retryDelay * retries;

  return (url: RequestInfo, init?: RequestInit): Promise<Response> => {
    let retries = 0;

    const retry = async (): Promise<Response> => {
      try {
        const userAgent =
          userAgents[Math.floor(Math.random() * userAgents.length)];
        return await fetch(url, {
          ...init,
          headers: {
            ...init?.headers,
            'User-Agent': userAgent,
            Host: 'www.outland.no',
          },
        });
      } catch (err) {
        if (retries < maxRetries) {
          retries++;
          await new Promise((resolve) => {
            setTimeout(resolve, timeout(retries));
          });
          return retry();
        }
        throw err;
      }
    };

    return retry();
  };
}
